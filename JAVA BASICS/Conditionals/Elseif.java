// A program to demonstrate the Else-if
 public class Elseif {

    public static void main(String args[])

{
    int mark=60;
    if(mark<40)
    {
        System.out.println("fail");
    }
    else if(mark>=40 && mark<80)
    {
        System.out.println("average");
    }
    else
    {
        System.out.println("Excellent");
    }
}}