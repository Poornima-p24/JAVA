public class Switch {

    public static void main(String args[])
    {
        int n=1;
        switch(n)
        {
            case 1:
                System.out.println("it is one");
                break; 
                 case 2:
                System.out.println("it is Two");
                break;  
                case 3:
                System.out.println("it is three");
                break;  
                case 4:
                System.out.println("it is four");
                break; 
                default:
                    System.out.println("Invalid");
                    break;
        }
    }
}